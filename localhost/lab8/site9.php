<h2>Корпоративные информационные порталы</h2>

<h3>Введение</h3>

<p>Портал (Portal) - сайт, организованный как системное многоуровневое объединение различных ресурсов и сервисов. Другими словами, портал - это "электронная библиотека", разделенная на различные тематические отделы, способные включать в себя количественные и качественные данные, анализы, графики и т.д., обновление которых происходит в реальном времени.</p>

<p>Информацию в порталах обычно организовывают по иерархическому признаку, связанному с определенной тематикой.</p>

<p>Привлекательность корпоративных порталов состоит в том, что они позволяют максимально приблизить бизнес к клиентам, партнерам и поставщикам, обеспечить пользователям персонализацию, "бесшовную" интеграцию информационной сущности компании и возможность устанавливать отношения внутри рабочих и информационных групп.</p>

<p>История создания информационных порталов</p>

<p>Рождение информационных порталов как самостоятельной информационной единицы можно датировать 1994-1995 гг., когда в сети Internet появился новый класс коммерческих сайтов - "информационные накопители" (Yahoo, AltaVista, Lycos и др).</p>

<p>С расширением Internet, эти сайты стали брать на себя роль путеводителя для пользователя Сети. Однако первое упоминание слова "портал" связывают с выпуском компанией Merrill Lynch в ноябре 1998 года отчета, в котором впервые было введено понятие корпоративного информационного портала (Enterprise Information Portal - EIP), под которым понимался пакет программного обеспечения, дающий компании возможность обеспечивать при помощи Web-браузера доступ к информации внутренних и внешних сетей. Серьезная потребность в построении EIP возникла при переходе корпоративных баз данных к сетевому формату, при необходимости систематизировать и усовершенствовать информационные хранилища компании. Тогда руководители поняли, что без единого информационного пространства, позволяющего и посетителям, и сотрудникам работать с данными компании и пользоваться аналитическими приложениями в режиме реального времени, не обойтись. Перспективность отросли информационных корпоративных порталов налицо, - по оценкам экспертов Merrill Lynch доходы с нее вырастут с 4,4 млрд. долларов в 1998 г. до 14,8 млрд. долларов в 2002 г., и свыше 80% американских фирм к 2001 г. построят собственные порталы.</p>

<p>Классификация порталов</p>

<p>Все порталы можно разделить на:</p>

<ol>
<li>Вертикальные (специализированные) порталы - узкой тематической направленности, посвященные определенной тематике;</li>
<li>Горизонтальные (универсальные) порталы - общего характера, обозревающие различные темы;</li>
<li>Потребительские порталы (Rambler, Yahoo, InfoArt и т.д.). Потребительские порталы содержат огромное количество ссылок на различные сайты, и служат для организации более удобного пребывания пользователя в Сети. Они содержат поисковые возможности, ленты новостей, биржевые сводки, прогнозы погоды, т.е. все, что необходимо для ежедневного информационного обеспечения. Кроме того, некоторые из них предоставляют пользователям услуги электронной почты, сервисы для планирования времени, ведения баз данных адресов и дат, а также другие бесплатные приложения. Тем самым они увеличивают количество посетителей, что является жизненно важным для них, поскольку потребительские порталы существуют за счет рекламной деятельности;</li>
<li>Торговые порталы, основной задачей которых является объединение вокруг себя наибольшего количества Web-узлов поставщиков и покупателей и централизация документооборота между ними. Все электронные транзакции в таких порталах проходят через электронные торговые площадки (marketplaces), соединяющие покупателей и поставщиков по "звездообразной" топологии;</li>
<li>Корпоративные порталы, предназначенные для организации единого информационного пространства внутри компании.</li>
</ol>

<p>Основные требования</p>

<p>Обслуживание большого числа пользователей;</p>

<p>Широкий спектр информации;</p>

<p>(Если первые два условия не были удовлетворены, то компании придется создавать несколько более мелких порталов, что сопряжено с дополнительными затратами на разработку и дальнейшее содержание, и, в итоге, порталы не выполняют своей основной цели - организации единого информационного пространства).</p>

<p>Поддержка основных сетевых форматов;</p>

<p>Возможности персонализации (настройка рабочего места пользователя в соответствии с его индивидуальными запросами, привычками и требованиями);</p>

<p>Возможности поиска. Реализация удобных и эффективных поисковых механизмов, используя в качестве источников различные информационные ресурсы (структурированные, неструктурированные, метаданные) и оценка достоверности и полноты полученных данных.</p>

<p>Обеспечение защиты хранящейся информации, используя программные и физические способы обеспечения безопасности (установление подлинности, управление доступом, конфиденциальность и целостность данных и т.д.);</p>

<p>Интеграция - обеспечение возможности взаимодействия сотрудников компании со всеми приложениями и информационными ресурсами (в соответствии с их приоритетом) через единый интерфейс.</p>

<p>Разбивка хранимой информации на категории (т.н. процесс категоризации). Автоматизированные процедуры категоризации результатов индивидуального поиска;</p>

<p>Приложения интеллектуального анализа (т.н. системы управления знаниями).</p>

<p>Структура корпоративных порталов</p>

<p>Описание работы информационного портала.</p>

<p>Процесс работы информационного портала можно разделить на несколько фаз:</p>

<ol>
<li>В портал из различных источников поступает информация.</li>
<li>Портал производит первичное распознание информации и предоставляет ей доступ.</li>
<li>При использовании систем управления знанием формируются метаданные.</li>
<li>Метаданные проходят через "фильтр", установленный пользователем, при этом ненужные данные отбрасываются.</li>
<li>После этого, отфильтрованные данные передаются пользователю. Пользователь также может настроить "фильтр" под свои личные требования.</li>
</ol>

<p>Корпоративный информационный портал разделен на две части:</p>

<p>Внутренний портал (Интранет), в котором расположена приватная информация, предназначенная для использования ограниченным числом пользователей. Используется локальная сеть.</p>

<p>Внешний портал, который доступен для общественного использования и в котором расположена публичная информация. Используется сеть Internet.</p>

<p>Функциональная структура корпоративного информационного портала может быть разбита на следующие составляющие:</p>

<ol>
<li>Приложения. В системе портала инструментом пользователя является браузер или навигатор, который обеспечивает ему доступ к страницам в необходимом формате. Многие порталы (например, Hummingbird Communications EIP) позволяют пользователю настроить домашнюю или персональную Web-страничку под его личные требования поиска и приема (т.н. механизм фильтрации данных).</li>
<li>Система обеспечения безопасности. Обычно включает в себя стандартные защитные меры, такие как: криптографическое кодирование, задание пароля, присваивание идентификатора, электронная цифровая подпись и т.д.</li>
<li>Приложения интеллектуального анализа или система управления знанием (Knowledge Management), которые представляют собой средства, позволяющие структурировать и категоризировать неучтенные данные (например, затерянные в почтовых ящиках), сформировать удобные механизмы доступа к ним и, используя специальные аналитические методы, сконструировать отчеты о собранной информации. Их принцип работы заключается в аннотировании существующих документов, формирование на базе этих аннотаций, описывающих документы, метаданных и их размещение в информационном хранилище вместе со ссылками на исходный документ. При этом устанавливается язык документа, его тема, дата создания и принадлежность. Одновременно создаются специальные словари или базы данных встречающихся в документе имен, терминов и других атрибутов с указанием их источника. Результатом деятельности является разбиение текстового документа на связанные категории, посредством эвристического анализа смысловой и терминологической близости распределяемых по иерархическим рубрикам текстов.</li>
<li>Репозитарий - хранилище метаданных об информационных объектах, пользователях, рабочих группах и т.д. Метаданные указывают тип объекта, раздел, к которому он принадлежит, формат документа и даже его содержание.</li>
<li>Подсистема публикаций и подписки, которая позволяет пользователям публиковать свои документы и подписываться на получение необходимой информации и внешних источников.</li>
<li>Механизмы анализа и планирования бизнес-процессов, в том числе системы планирования ресурсов предприятия (Enterprise Resource Planning - ERP). Системы ERP - набор интегрированных приложений, которые в едином информационном пространстве поддерживают все основные аспекты управленческой деятельности предприятия. Среди них можно назвать планирование ресурсов для производства товаров и услуг, оперативное управление производственным планом, виды учета и анализа результатов экономической деятельности. Все операции планирования и анализа в ERP разбиты на отдельные функциональные модули.</li>
<li>Модуль CRM - Customer Relationship Management (может входить в систему ERP), который обеспечивает объединение front и back-office компании, позволяя сформировать информационную базу с данными о клиентах, службу информационной поддержки клиентов, настраивать портал под требование клиента (например, выдавать пользователю ассортимент товаров или список информации, основываясь на прошлых обращениях) и т.д. При этом предприятие может не только значительно повысить потенциал front-office и упорядочить процесс документооборота с оппонентом, но и производить исследования предпочтений покупателей. Однако основной функцией CRM является "удержание" клиента, что становится первостепенной задачей для любого бизнеса (по мнению экспертов, "удержание" 5% покупателей дает прирост прибыли на 25-125%).</li>
</ol>

<p>Преимущества порталов</p>

<p>Преимущества, которые предоставляют информационные порталы:</p>

<p>Корпоративные порталы структурируют данные, находящиеся в базе данных корпорации и предоставляют их в удобной для использования форме.</p>

<p>Порталы отслеживают и извлекают информацию из сети Internet и фильтруют ее, основываясь на требованиях пользователей.</p>

<p>Корпоративные порталы обеспечивают всеобщий доступ к публичной информации, основываясь на возможностях Сети. Реклама при использовании внутренних возможностей компании.</p>

<p>Использование возможностей гипертекста для перемещения по ссылкам, вставленным прямо в текст.</p>

<p>Использование графических, аудио и видео образцов;</p>

<p>Использование одной универсальной программы - Web-браузера - для работы со всеми видами информации.</p>

<p>Примеры</p>

<p>Пакет EIP компании Hummingbird Communication</p>

<p>В основе построения портала лежит технология "тонкого клиента", когда пользователь с помощью Web-браузера обращается к html-серверу, который обеспечивает доступ к внутренним и внешним ресурсам компании. При этом используется удобная организация информационного интерфейса - слева расположено оглавление всех доступных ресурсов, а справа находится окно информационного обмена, в котором появляются вызываемые данные.</p>

<p>Процесс персонализации построен на использовании хранилища метаданных, на основе которого каждым пользователем создается "личный портфель" (personal portfolio) - настраиваемые Java-модули. Кроме того, они являются носителями информации о полномочиях пользователя, настройках окна браузера и т.д. При помощи этих средств пользователь может настроить системы на прием и отображение необходимых ему данных.</p>

<p>Кроме стандартных средств, пользователь может создавать собственные папки и формировать в них индивидуальные интерфейсы для взаимодействия с внешними ресурсами. При этом используются средства E-Clip, обеспечивающие взаимодействие портала с конкретными ресурсами и приложениями.</p>

<p>Среди средств, поддерживаемых порталом, можно выделить:</p>

<p>Системы управления документами (CyberDOCS, Documentum и др.);</p>

<p>Почтовые службы (Lotus Notes, MS Exchange);</p>

<p>Системы планирования ресурсов предприятий - ERP (SAP, R/3);</p>

<p>Системы управления знаниями;</p>

<p>Хранилища данных и др.</p>

<p>DataChannel Server</p>

<p>Базовый сервер DataChannel Server (DCS), выпущенный компанией DataChannel (www.datachannel.com) является программным фундаментом для построения корпоративного информационного портала. DCS базируется на использовании стандартов Document Object Model (DOM), XML и JAVA.</p>

<p>Его общая структура разделена на три функциональных компонента: уровень данных, уровень приложений и уровень информационного доступа. Каждый из них отвечает за определенные функции: на уровне данных, например, происходит первичная обработка и хранение полученных из внешних каналов данных. При этом пользователь взаимодействует только с уровнем доступа, где службы каталогов, поддержки хранилищ информации, подписки и администрирования, в соответствии с его приоритетом, обеспечивают извлечение необходимых пользователю данных и выполнение служебных процедур.</p>

<p>Поддерживается обработка около 200 форматов документов. Возможен обычный поиск (поиск по всей Сети) и расширенный, с использованием подготовленных пользователем индексов.</p>

<p>Sybase Enterprise Portal</p>

<p>Корпоративный портал Sybase использует технологию "тонкого клиента" для общения пользователя с информационными ресурсами. Sybase привязывает стандарты JAVA и XML к базам данных, сообщений и событий и к промежуточному ПО.</p>

<p>Фундаментом для построения корпоративного портала Sybase служит Sybase Enterprise Application Server (EAServer), который действует как хост дополнительных сервисов, таких как авторизация и генерация профиля пользователя. Он может предоставить "бесшовный" доступ портала к данным, информации, событиям и приложениям.</p>

<p>EAServer позволяет ориентировать деятельность портала на профиль бизнеса.</p>

<p>Первый подобный продукт - Sybase Financial Server - поддерживает приложения для интерактивного осуществления банковских операций и брокерской деятельности через сеть Internet.</p>

<p>Решения Sybase позволяют не только извлекать корпоративные данные, они способны также интегрировать данные с другими приложениями и унаследованными системами, создавая единое представление данных для пользователей. Кроме того, Sybase Adaptive Server Enterprise 12.0 даст возможность пользователям расширить функциональные системы управления операциями, чтобы подключить их содержательную часть к корпоративному порталу.</p>
